function describeCountry(country,population,capitalCity) {
return (`${country} has ${population} million people and its capital city is ${capitalCity}`)
}

let china = describeCountry('China',21.5,'Kinshasa');
let india = describeCountry('India',14.2,'New Delhi');
let japan= describeCountry('Japan',13.9,'Tokio');

console.log(china);
console.log(india);
console.log(japan);

